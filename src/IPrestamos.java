    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean banderaLibro = false;
        boolean banderaUsuario = false;
        for(Libro lib: libros){
            if(lib.getIdLibro() == Integer.parseInt(jTextField4.getText()) & lib.isEstatus() == true){
                banderaLibro = true;
            }
        }
        for(Usuario us: usuarios){
            if(us.getIdUsuario() == Integer.parseInt(jTextField5.getText())){
                banderaUsuario = true;
            }
        }
        
        if(banderaLibro & banderaUsuario){
            try {
                // TODO add your handling code here:
                Prestamo p = new Prestamo(Integer.parseInt(jTextField1.getText()),jTextField2.getText(),
                    jTextField3.getText(), Integer.parseInt(jTextField4.getText()),
                    Integer.parseInt(jTextField5.getText()),libros, jTextArea1);
                prestamos.add(p);
                JOptionPane.showMessageDialog(null,"Se presto el libro y se ha agregado el prestamo");
                System.out.println(prestamos);  
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
            } 
            catch (ParseException ex) {
                Logger.getLogger(IPrestamos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            if(!banderaLibro)
                JOptionPane.showMessageDialog(null, "Libro no disponible");
            if(!banderaUsuario)
                JOptionPane.showMessageDialog(null, "El usuario no existe");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

